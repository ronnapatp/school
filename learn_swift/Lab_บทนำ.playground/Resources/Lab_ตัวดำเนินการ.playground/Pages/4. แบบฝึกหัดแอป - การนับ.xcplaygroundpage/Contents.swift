## แบบฝึกหัดแอป - การนับ

 >แบบฝึกหัดเหล่านี้เป็นการเน้นย้ำแนวคิดการใช้งาน Swift ในบริบทของแอปการติดตามข้อมูลฟิตเนส
 
 คุณสมบัติพื้นฐานที่สุดของแอปการติดตามข้อมูลฟิตเนสของคุณคือการนับจำนวนก้าว ให้สร้างตัวแปร `steps` แล้วตั้งค่าให้เท่ากับ 0 จากนั้นเพิ่มค่าของตัวแปรนี้ขึ้น 1 หน่วย เพื่อจำลองการก้าวเท้าของผู้ใช้
