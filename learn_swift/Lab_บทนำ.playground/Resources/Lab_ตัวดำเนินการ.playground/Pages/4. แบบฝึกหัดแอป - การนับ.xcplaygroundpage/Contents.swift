/*:
 นอกจากการติดตามจำนวนก้าวแล้ว แอปการติดตามข้อมูลฟิตเนสของคุณยังติดตามระยะทางที่เดินทางอีกด้วย ให้สร้างตัวแปร `distance` ของประเภท `Double` และตั้งค่าให้เท่ากับ 50 ซึ่งจะแสดงว่าผู้ใช้เดินทางไปแล้ว 50 ฟุต
 
 แต่คุณตัดสินใจว่าจะแสดงระยะทางเป็นเมตรแทน 1 เมตรเท่ากับ 3 ฟุตโดยประมาณ ใช้ตัวดำเนินการกำหนดค่าแบบผสมเพื่อแปลง `distance` เป็นเมตร แสดงผลลัพธ์
 */


